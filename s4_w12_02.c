#include <stdio.h>

/**
 * @brief ฟังก์ชัน swap รับ pointer ของจำนวนเต็มสองตัว (*a และ *b)
 * และทำการสลับค่าที่ตัวแปรต้นฉบับชี้อยู่
 * * @param a pointer ชี้ไปยังจำนวนเต็มตัวแรก
 * @param b pointer ชี้ไปยังจำนวนเต็มตัวที่สอง
 */
// TODO: เขียนฟังก์ชัน swap ที่รับ int *a, int *b แล้วสลับค่า
void swap(int *a, int *b) {
    // ประกาศตัวแปรชั่วคราว temp เพื่อเก็บค่าของ *a (ค่าที่ a ชี้อยู่)
    // การทำเช่นนี้เป็นการ "สำรอง" ค่าหนึ่งไว้ก่อนสลับ
    int temp = *a;
    
    // กำหนดค่าที่ *b ชี้อยู่ (คือค่าของตัวแปร b ใน main) ให้กับตัวแปรที่ *a ชี้อยู่ 
    // (คือตัวแปร a ใน main) 
    // ตอนนี้ตัวแปร a ใน main มีค่าใหม่เป็น 10
    *a = *b;
    
    // กำหนดค่าที่เก็บไว้ใน temp (ซึ่งคือค่าเริ่มต้นของตัวแปร a ใน main คือ 5) 
    // ให้กับตัวแปรที่ *b ชี้อยู่ (คือตัวแปร b ใน main)
    // ตอนนี้ตัวแปร b ใน main มีค่าใหม่เป็น 5
    *b = temp;
    
    // เมื่อฟังก์ชันนี้ทำงานเสร็จ ตัวแปร a และ b ใน main จะมีค่าสลับกันอย่างถาวร
    // เพราะเราได้ส่ง "ที่อยู่" (address) ของตัวแปรเข้ามาทำงาน
}

int main() {
    // ประกาศและกำหนดค่าเริ่มต้นให้กับตัวแปรจำนวนเต็มสองตัว
    int a = 5, b = 10;

    // แสดงค่าก่อนสลับ
    printf("Before swap: a = %d, b = %d\n", a, b);

    // เรียกใช้ swap โดยส่ง "ที่อยู่" (address) ของตัวแปร a และ b ด้วยเครื่องหมาย &
    // ฟังก์ชัน swap จะได้รับ pointer ที่ชี้ไปยังตัวแปร a และ b ใน main()
    swap(&a, &b);

    // แสดงค่าหลังสลับ
    // ค่าของ a และ b ได้ถูกเปลี่ยนแปลงถาวรจากภายในฟังก์ชัน swap()
    printf("After swap: a = %d, b = %d\n", a, b);

    return 0;
}
/*
ptr = &x; - ให้ pointer ชี้ไปที่ตัวแปร x

ptr = 20; - เปลี่ยนค่าที่ address ของ X ผ่าน pointer

ทำให้ค่า x เปลี่ยนจาก 10 เป็น 20*/